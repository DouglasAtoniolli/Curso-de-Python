#Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.
n = int(input('Digite aqui um numero inteiro de sua escolha!!!'))
dobro = n * 2
triplo = n * 3
raiz = n ** (1/2)
print('O numero que voce digitou foi {}, seu dobro é {}, e seu triplo {} e a raiz quadrada {}'
      .format(n,dobro,triplo,raiz))



