#Faça um programa que leia um número Inteiro e mostre na tela o seu sucessor e seu antecessor.
n = int(input('Digite aqui um numero inteiro de sua escolha !!!'))
antecessor = n-1
sucessor = n + 1
print('O numero que voce digitou foi {}, e seu antecessor é {}, e o seu sucessor é {}'.format(n,antecessor,sucessor))

